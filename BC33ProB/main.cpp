int GCD(int m, int n)
{
    if(!m) return n;
    return GCD(n%m, m);//yushu and chushu
}


LL MultMod(LL a,LL b,LL MOD){
    a %= MOD;
    b %= MOD;
    LL ret = 0;
    while(b){
        if (b & 1){
            ret += a;
            if(ret >= MOD) ret -= MOD;
        }
        a = a << 1;
        if (a >= MOD) a -= MOD;
        b = b >> 1;
    }
    return ret;
}
LL MOD;
LL F(LL n)
{
    if(!n) return 0;
    LL sum=F(n/2);
    if(n&1) return 2*MultMod(sum, sum, MOD);
    else return MultMod(sum, sum, MOD);
}
